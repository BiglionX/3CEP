// 主函数
async function main() {
  console.log('🔧 开始修复 procurement-intelligence 模块编码问题...\n');
  
  let successCount = 0;
  let errorCount = 0;
  
  for (const file of filesToFix) {
    try {
      if (fixFile(file)) {
        successCount++;
      }
    } catch (error) {
      console.error(`❌ 处理 ${file} 时出错:`, error.message);
      errorCount++;
    }
  }
  
  console.log('\n========================================');
  console.log(`修复完成！`);
  console.log(`✅ 成功：${successCount} 个文件`);
  console.log(`❌ 失败：${errorCount} 个文件`);
  console.log('========================================');
  
  process.exit(errorCount > 0 ? 1 : 0);
}

main();
